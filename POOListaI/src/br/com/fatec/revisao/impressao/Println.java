package br.com.fatec.revisao.impressao;

public class Println {

	public static void main(String[] args) {
		/*
		 * Println Função: Gera uma saída de texto entre aspas duplas significando uma
		 * String, criando uma nova linha e posicionando o cursor na linha de baixo, o
		 * que é identificado pela terminação "ln".
		 */
		System.out.println("Olá, mundo!");
		System.out.println("Hello, world!");

		/*
		 * Saída: 
		 * Olá, mundo! 
		 * Hello, world!
		 */

	}

}
