package br.com.fatec.revisao.impressao;

public class Print {

	public static void main(String[] args) {
		/*
		 * Print
		 * Função: Exibe uma String sem criar uma nova linha,
		 * deixando o cursor na mesma linha.
		 */
		
		System.out.print("Olá, mundo!");
		System.out.print("Hello, world!");
		
		//Saída: Olá, mundo!Hello, world!

	}

}
